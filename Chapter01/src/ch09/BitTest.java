package ch09;

public class BitTest {

	public static void main(String[] args) {
		
		int Num1 = 5;
		int Num2 = 10;
		
		System.out.println(Num1 | Num2);
		System.out.println(Num1 & Num2);
		System.out.println(Num1 ^ Num2);
		System.out.println(~Num1);
		
		System.out.println(Num1 << 2);
		System.out.println(Num1);
		System.out.println(Num1<<=2);
		System.out.println(Num1);
	}

}
